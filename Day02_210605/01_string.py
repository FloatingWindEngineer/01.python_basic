#=======================복습 1 (아래)==========================================
print(123)
print(123+456)
print('KOREA' + 'SEOUL')   #문자열 더하기-> 붙여서 출력됨 -> 작은 따옴표로 가능
print("KOREA" + "SEOUL")   #문자열 더하기 -> 큰따옴표로도 가능
print('123'+'456')

msg1="SEOUL"
msg2='JEJU'
print(msg1+msg2)           #문자열 연결

