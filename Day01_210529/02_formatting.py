#성적프로그램
name='손흥민'
kor=80
eng=85
mat=95

total=kor+eng+mat
aver=total/3

print("이름:%s" % name)  #출력값 : 이름:손흥민
print("국어:%d" % kor)   #출력값 : 국어:80
print("영어:%d" % eng)   #출력값 : 영어:85
print("수학:%d" % mat)   #출력값 : 수학:95
print("총점:%d" % total) #출력값 : 총점:260
print("평균:%.2f" %aver) #출력값 : 평균:86.67
