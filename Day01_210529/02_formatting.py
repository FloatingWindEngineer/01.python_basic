print("'")

#-----------------------------------------------------------
"""
    정수      %d
    실수      %f
    문자열    %s
"""
print('나이:%d' % 25)
print('키:%f' % 178.5)   #기본 출력은 소수점 6자리까지 출력됨?
print('이름:%s' % '박지성')
#-----------------------------------------------------------

print('@%d@' % 123)     
print('@%5d@' % 123)    #5자리, 오른쪽 정렬         : @  123@
print('@%-5d@' % 123)   #5자리, 왼쪽정렬            : @123  @
print('@%05d@' % 123)   #5자리, 빈자리 0으로 채워라  : @00123@

print()   #줄바꿈 하라

money=123.456789
print('@%f@' % money)   #출력값 : @123.456789@
print('@%10.3f@' % money)   #양수: 오른쪽 정렬 / .3: 소수점 3째 자리까지 / 출력값 : @   123.457@
print('@%-10.3f@' % money)  #음수: 왼쪽 정렬 / .3: 소수점 3째 자리까지 / 출력값: @123.457   @
print('@%.3f@' % money)  #출력값 : @123.457@

print()

print('@%s@' % 'KOREA')
print('@%10s@' % 'KOREA')
print('@%-10s@' % 'KOREA')
