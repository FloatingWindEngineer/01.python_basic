#print() -> 콘솔창(모니터) 출력하기 위한 함수

"""
    Data type(자료형)
    - 숫자형 : 정수, 실수
    - 문자형 : ' 또는 " 로 감싼다
    - 논리형(boolean형) : True/False (맞다/틀리다 판단) 
    - 날짜형 : 년월일시분초
    - 연속성 데이터 : 리스트, 딕셔너리, 튜플, 데이터프레임, 시리즈 etc.
                     리스트, 딕셔너리, 데이터프레임(엑셀이랑 비슷) 많이 사용함
"""
###############################################################

#숫자형
print(123)      #양의 정수  -> 이건 숫자
print(-456)     #음의 정수  -> 이건 숫자
print(1.2)      #실수
print(-3.4)     #실수
print(5.0)      #실수

#문자형(문자열형)
print('KOREA')
print('SEOUL')
print('123')     #문자  -> 이건 따옴표에 있으므로 숫자지만 문자형임
print('-456')    #문자

#논리형(boolean)
print(True)      #논리형 True 첫 문자는 대문자 필수
print(False)     #논리형 False 첫 문자는 대문자 필수
print('True')    #문자 -> 이건 따옴표에 있으므로 문자형임

print('-' * 30)    #'-'기호 30개
################################################################

#상수 constant
#-> 절대 변하지 않는 고정 불변의 값
print(3)
