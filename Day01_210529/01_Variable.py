#변수 variable
#-> 변하는 값
#-> 저장(대입) 연산자 = 

#식별자 : 변수명, 함수명, 클래스명, 모듈 등등
#식별자 작성 규칙
#-> 의미를 부여해서 영문자, 숫자등을 조합해서 작성한다
#-> 한글변수명, 첫글자로 숫자는 사용불가

a=3      #'a라는 문자에 3을 적용하라'는 의미
#python에서는 숫자가 입력되면 변수의 타입이 결정됨
#JAVA는 타입(정수or실수)을 정해주고 그에 맞는 값을 입력해야 함
print(a)     #3이 출력됨 -> 변수
print('a')   #a가 출력됨 -> 상수

b=5
print(b)
print(a+b)    #a=3, b=5 가 적용되서 결과값 8이 출력됨
print('a+b')  #a+b 라는 문자가 출력됨


a=2
